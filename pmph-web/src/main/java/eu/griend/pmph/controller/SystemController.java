package eu.griend.pmph.controller;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.RequestMapping;

import eu.griend.pmph.service.PamphletListener;

@Controller
public class SystemController {

	@Autowired
	private PamphletListener pamphletListener = null;

	@RequestMapping("/system")
	public String systems(String name, Model model) {
		model.addAttribute("systems", this.pamphletListener.getSystemEntities());
		
		return "system";
	}
}
